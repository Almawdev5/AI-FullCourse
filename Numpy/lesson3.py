import numpy as np

# 1D array
arr = np.array([10,20,30,40,50])

print("Array:")
print(arr)

print("\nDimensions:")
print(arr.ndim)

print("\nShape:")
print(arr.shape)

print("\nSize:")
print(arr.size)

print("\nData Type:")
print(arr.dtype)


# 2D array

matrix = np.array([
    [1,2,3],
    [4,5,6]
])

print("\nMatrix:")
print(matrix)

print("\nMatrix Dimensions:")
print(matrix.ndim)

print("\nMatrix Shape:")
print(matrix.shape)

print("\nMatrix Size:")
print(matrix.size)
